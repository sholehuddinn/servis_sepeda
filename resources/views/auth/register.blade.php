        // Toggle password visibility
        function togglePassword(fieldId) {
            const passwordInput = document.getElementById(fieldId);
            const eyeIcon = document.querySelector(`#${fieldId} + button i`);
            
            if (passwordInput.type === 'password') {
                passwordInput.type = 'text';
                eyeIcon.classList.remove('fa-eye');
                eyeIcon.classList.add('fa-eye-slash');
            } else {
                passwordInput.type = 'password';
                eyeIcon.classList.remove('fa-eye-slash');
                eyeIcon.classList.add('fa-eye');
            }
        }

        // Password strength checker
        document.getElementById('password').addEventListener('input', function() {
            const password = this.value;
            const strengthBars = [
                document.getElementById('strength-bar-1'),
                document.getElementById('strength-bar-2'),
                document.getElementById('strength-bar-3'),
                document.getElementById('strength-bar-4')
            ];
            const strengthText = document.getElementById('password-strength-text');
            
            let strength = 0;
            
            // Reset bars
            strengthBars.forEach(bar => {
                bar.classList.remove('bg-red-500', 'bg-yellow-500', 'bg-green-400', 'bg-green-600');
                bar.classList.add('bg-gray-200');
            });
            
            // Check strength
            if (password.length >= 8) strength++;
            if (/[A-Z]/.test(password)) strength++;
            if (/[0-9]/.test(password)) strength++;
            if (/[^A-Za-z0-9]/.test(password)) strength++;
            
            // Update bars
            for (let i = 0; i < strength; i++) {
                if (i < 2) {
                    strengthBars[i].classList.remove('bg-gray-200');
                    strengthBars[i].classList.add('bg-red-500');
                } else if (i === 2) {
                    strengthBars[i].classList.remove('bg-gray-200');
                    strengthBars[i].classList.add('bg-yellow-500');
                } else {
                    strengthBars[i].classList.remove('bg-gray-200');
                    strengthBars[i].classList.add('bg-green-600');
                }
            }
            
            // Update text
            const texts = ['Sangat Lemah', 'Lemah', 'Cukup', 'Kuat', 'Sangat Kuat'];
            strengthText.textContent = `Kekuatan password: ${texts[strength]}`;
            strengthText.className = 'text-xs mt-1 ' + 
                (strength < 2 ? 'text-red-600' : 
                 strength < 3 ? 'text-yellow-600' : 
                 'text-green-600');
        });

        // Confirm password match checker
        function checkPasswordMatch() {
            const password = document.getElementById('password').value;
            const confirmPassword = document.getElementById('password_confirmation').value;
            const checkIcon = document.getElementById('password-match-check');
            const matchText = document.getElementById('password-match-text');
            
            if (confirmPassword.length === 0) {
                checkIcon.classList.add('opacity-0');
                matchText.textContent = 'Password harus sama';
                matchText.className = 'text-xs text-gray-500';
                return false;
            }
            
            if (password === confirmPassword) {
                checkIcon.classList.remove('opacity-0');
                matchText.textContent = 'Password cocok';
                matchText.className = 'text-xs text-green-600';
                return true;
            } else {
                checkIcon.classList.add('opacity-0');
                matchText.textContent = 'Password tidak cocok';
                matchText.className = 'text-xs text-red-600';
                return false;
            }
        }

        document.getElementById('password_confirmation').addEventListener('input', checkPasswordMatch);
        document.getElementById('password').addEventListener('input', checkPasswordMatch);

        // Form field validation indicators
        document.getElementById('name').addEventListener('input', function() {
            const checkIcon = document.getElementById('name-check');
            if (this.value.length > 2) {
                checkIcon.classList.remove('opacity-0');
            } else {
                checkIcon.classList.add('opacity-0');
            }
        });

        document.getElementById('email').addEventListener('input', function() {
            const checkIcon = document.getElementById('email-check');
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            if (emailRegex.test(this.value)) {
                checkIcon.classList.remove('opacity-0');
            } else {
                checkIcon.classList.add('opacity-0');
            }
        });

        // Form submission validation
        document.querySelector('form').addEventListener('submit', function(e) {
            const password = document.getElementById('password').value;
            const confirmPassword = document.getElementById('password_confirmation').value;
            const terms = document.getElementById('terms').checked;
            
            if (!terms) {
                e.preventDefault();
                alert('Anda harus menyetujui Syarat & Ketentuan untuk mendaftar.');
                return;
            }
            
            if (password !== confirmPassword) {
                e.preventDefault();
                alert('Password dan konfirmasi password tidak cocok.');
                return;
            }
            
            if (password.length < 8) {
                e.preventDefault();
                alert('Password minimal harus 8 karakter.');
                return;
            }
            
            // Show loading state
            const button = document.getElementById('register-button');
            button.innerHTML = `
